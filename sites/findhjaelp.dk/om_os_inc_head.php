 <p><strong><u>Om Findhjælp.dk</u></strong> <br />
    <br />
 
    <strong>Formål<br />
    </strong>Formålet med findhjælp.dk er at give et samlet overblik over steder i Danmark, hvor udsatte og sårbare børn og unge kan finde offline voksenhjælp. 
   </p>
 <p><strong>Krav til henvisningssteder på findhjælp.dk <br />
     </strong>Alle tilbud på findhjælp.dk skal opfylde følgende, af Cyberhus definerede krav, og Cyberhus påberåber sig ret til at afvise eller slette tilbud som ikke opfylder disse krav: </p>
 <ol>
   <li>tilbuddet skal henvende sig til børn og unge</li>
   <li>tilbuddet skal være omkostningsfrit for børn og unge</li>
   <li>tilbuddet skal kunne tilgåes af børn og unge uden forudgående visitation</li>
   <li>tilbuddet skal være funderet i ønsket om at hjælpe børn og unge i forhold til en eller flere problemstillinger, uafhængigt af barnets/den unges sociale, kulturelle og religiøse baggrund. </li>
</ol>
 <p>De enkelte tilbud indtaster selv deres oplysninger, som efterfølgende bliver verificeret, i forhold til indhold og beskrivelse af tilbuddet, af en pædagogisk ansvarlig koordinator på Cyberhus. Verificeringen sker ud fra de oplysninger, som det enkelte rådgivningstilbud stiller til rådighed.  Tilbud der er funderet i foreninger, organisationer eller hos enkeltstående personer, som Cyberhus ikke har forudgående kendskab til skal, i tillæg til oplysningerne der fremgår på findhjælp.dk, udfylde et verificeringsskema med følgende oplysninger: </p>
 <ol>
   <li>Beskrivelse af idégrundlag og formål</li>
   <li> Beskrivelse af målgruppe og de voksne der har den  direkte kontakt til målgruppen</li>
   <li>Samarbejdspartnere og/eller referencer</li>
 </ol>
 <p>Målet med verificeringsprocessen er at skabe højest mulig sikkerhed omkring kvalitet og relevans af det enkelte tilbud for de børn og unge der henvises via findhjælp.dk og cyberhus.dk.</p>
<p>Cyberhus har ingen direkte indflydelse eller ansvar for de konkrete forløb det enkelte tilbud praktiserer. 
  </p>
<p><u><strong>Samarbejde med Cyberhus</strong></u> </p>
<p>Findhjælp.dk er en integreret del af det rådgivende arbejde i Cyberhus. Vi betragter derfor de rådgivningstilbud som findes på findhjælp.dk, som offline samarbejdspartnere omkring det fælles mål at gøre en forskel for udsatte børn og unge. Samarbejdet er således udelukkende funderet i den fælles indsats for at hjælpe børn og unge, og ikke hverken et økonomisk partnerskab eller en binding til at gennemføre fælles projekter eller arbejdsmetoder. Vi forbeholder os dog ret til i Cyberhus at omtale de deltagende tilbud som samarbejdspartnere, selvfølgeligt uden at dette binder tilbuddet på nogen måde. <strong> </strong></p>
<p><strong><u>Om Cyberhus</u></strong>   </p>
<p><strong>Hvad er Cyberhus</strong> <br />
  Cyberhus er et online klubhus og anonym rådgivningstjeneste for udsatte børn og unge. Cyberhus har taget initiativ til Findhjælp.dk på baggrund af de mange henvisninger vi til dagligt giver børn og unge til hjælpeforanstaltninger i deres nærområde. Det er således Cyberhus der moderer og løbende udvikler Findhjælp.dk.  
  Cyberhus er organiseret i landsorganisationen Ungdommens Vel og er en frivillig organisation. Derved sikres uafhængigheden af offentlige myndigheder og friheden til at forfølge de mål, som følger af vores mission og vision. Vi har et særdeles godt samarbejde med kommuner, ministerier og andre offentlige myndigheder.  </p>
<p><strong>Cyberhus' målgruppe</strong> <br />
Den primære aldersgruppe er fra ca. 9 til 18 år med hovedvægt på børn og unge i alderen 12 - 15 år. </p>
  <ol>
    <li>Børn og unge med pludseligt opståede, forholdsvis forbigående, personlige eller relationsmæssige problemer. Dette drejer sig om børn og unge som kommer fra relativt velfungerende hjem, hvor der bl.a. kan være tale om problemer omhandlende mobning, kærlighed, sex, pubertet o.lign. </li>
    <li>Marginaliserede og/eller belastede børn og unge</li>
  </ol>
  Her er tale om børn og unge, hvis hverdag har været eller kan være præget af vold, seksuelt overgreb, stofmisbrug og/eller kriminalitet, manglende tilknytning til skole og manglende positive voksenrelationer.  
     
  <p><strong>Rådgivning</strong> <br />
  Cyberhus tilbyder netbaseret rådgivning via chat eller e-mail, og henviser i den forbindelse børnene til hjælpeforanstaltninger overalt i Danmark. Vi har taget initiativ til Findhjælp.dk for at bygge bro mellem den hjælp sårbare børn og unge kan få hos os online, og den hjælp de kan finde lokalt offline. Vores rådgivning er åben og anonym. Alle rådgivere i Cyberhus er voksne, med relevant faglig baggrund. </p>
